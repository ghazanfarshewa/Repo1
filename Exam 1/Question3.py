# Perform access list, change list, add list and remove list methodes

myList = ["1","2","3","4","5"]

# This is for access
print(myList[0]) 

# This is for changing the list
myList[0] = 20
print(myList)

# Below is for add in list
x1 = 19
myList.append(x1)
print(myList)

# Below is for the remove of item from list

print(myList.remove[1])